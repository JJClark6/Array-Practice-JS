var array = ["Banana", "Apples", "Oranges", "Blueberries"];

/* Remove Banana from array */
array.shift();

/* Sort array in order */
array.sort();

/* Put Kiwi at the end of the array */
array.push("Kiwi");

/* Remove Apples from array */
array.shift();

/* Sort in reverse order */
array.reverse();
